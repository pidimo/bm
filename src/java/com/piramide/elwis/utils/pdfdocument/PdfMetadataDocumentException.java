public class PdfMetadataDocumentException extends Exception {
    public PdfMetadataDocumentException() {
        super();
    }

    public PdfMetadataDocumentException(String message) {
        super(message);
    }

    public PdfMetadataDocumentException(Throwable cause) {
        super(cause);
    }

    public PdfMetadataDocumentException(String message, Throwable cause) {
        super(message, cause);
    }
}
